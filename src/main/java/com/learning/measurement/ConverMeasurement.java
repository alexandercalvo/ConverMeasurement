package com.learning.measurement;

public class ConverMeasurement {

	public double toCentimeter(double measure) {
		
		return measure*100;
	}
	
	public double toMillimeter(double measure) {
		return measure*1000;
	}
	
	public double toFoot(double measure) {
		return measure*3.328084;
	}
	
	public double toInches(double measure) {
		return measure*39.701;
	}
	
	public double toYard(double measure) {
		return measure*1.09361;
	}
	
	public double toligthYear(double measure) {
		return measure*1.057;
	}
	
	public double toAngstrom(double measure) {
		return measure*1;
	}
}
